package com.codegym.task.task23.task2312;

public class Mouse {
    private int y;
    private int x;


    public Mouse() {

    }

    public Mouse(int x, int y) {
        this.x = x;
        this.y = y;
    }
    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
